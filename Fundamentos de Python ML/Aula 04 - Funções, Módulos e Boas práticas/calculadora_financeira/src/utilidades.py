def soma(a, b):
    """
    Retorna a soma de dois números.

    Parâmetros:
        a (int ou float): Primeiro número.
        b (int ou float): Segundo número.

    Retorna:
        int ou float: A soma de a e b.
    """
    return a + b

def multiplica(a, b):
    """
    Retorna o produto de dois números.

    Parâmetros:
        a (int ou float): Primeiro número.
        b (int ou float): Segundo número.

    Retorna:
        int ou float: O produto de a e b.
    """
    return a * b
